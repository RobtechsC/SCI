    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        try {
            if (isActualizacion){
                SimpleDateFormat sf=new SimpleDateFormat("dd-MM-yyyy");
                Date fecha=sf.parse(jTxtFecha.getText());
                java.sql.Date date=new java.sql.Date(fecha.getTime());
                datos.setFecha(date);
                datos.setPlantel(idPlantel);
                datos.setUnidad(unidad);
                datos.setCantidadCuartosGalon(Integer.parseInt(jTxtCantidadGalones.getText()));
                if (datos.updateRegister(consecutivoOld)){
                    clearData();
                }
            }
            else{
            SimpleDateFormat sf=new SimpleDateFormat("dd-MM-yyyy");
            Date fecha=sf.parse(jTxtFecha.getText());
            java.sql.Date date=new java.sql.Date(fecha.getTime());
            datos.setFecha(date);
            datos.setPlantel(idPlantel);
            datos.setUnidad(unidad);
            datos.setCantidadCuartosGalon(Integer.parseInt(jTxtCantidadGalones.getText()));
            if (datos.saveDataMySql())
            {            
                clearData();
            }
            }
        } catch (ParseException ex) {
            Logger.getLogger(Control_Aceites.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed
    public void getListLugares(){
        final ResultSet rs=modulo.Listar("Select * from Planteles_Empresa where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'");
        if (rs!=null){
            try {
                while(rs.next()){
                 jComboBoxLugares.addItem(rs.getString("Plantel"));
                  
                }
                jComboBoxLugares.addItemListener(new ItemListener() {

                    @Override
                    public void itemStateChanged(ItemEvent e) {
                        try {
                            int index=jComboBoxLugares.getSelectedIndex()+1;
                            rs.absolute(index);
                            idPlantel=rs.getInt("IdPlantel");
                            //JOptionPane.showMessageDialog(rootPane, idPlantel);
                        } catch (SQLException ex) {
                            Logger.getLogger(Control_Aceites.class.getName()).log(Level.SEVERE, null, ex);
                            JOptionPane.showMessageDialog(rootPane, "ERROR!!!! No se pudo comunicar con el servidor. Por favor verifique que tiene conexion a internet e intente nuevamente "+ex.getMessage());
                        }
                       
                    }
                });
                rs.first();
                idPlantel=rs.getInt("IdPlantel");
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "ERROR!!!! No se pudo comunicar con el servidor. Por favor verifique que tiene conexion a internet e intente nuevamente "+ex.getMessage());
            }
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "ERROR!!!! No se pudo comunicar con el servidor. Por favor verifique que tiene conexion a internet e intente nuevamente");
        }
    }
