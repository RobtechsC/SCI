    public void setListenerList(){
        jListUsuarios.addListSelectionListener(new ListSelectionListener() {

            @Override
            public void valueChanged(ListSelectionEvent e) {
                AdministracionUsuarios.setUser(jListUsuarios.getSelectedValue().toString());
                getPermisosUser(jListUsuarios.getSelectedValue().toString());
                jTituloUsuario.setText("USUARIO SELECCIONADO: "+jListUsuarios.getSelectedValue().toString().toUpperCase());
            }
        });
    }
    public void getPermisosUser(String user){
        try {
            permisosusuarios=modulo.Listar("Select * from ListadoUsuariosxEmpresa where usuario='"+user+"'");
            if (permisosusuarios.first()){
                jIngresoAceites.setSelected(permisosusuarios.getBoolean("ingresos_control_aceites"));
                jIngresoBaterias.setSelected(permisosusuarios.getBoolean("ingresos_control_baterias"));
                jIngresoLlantas.setSelected(permisosusuarios.getBoolean("ingreso_control_llantas"));
                jCreacionOrden.setSelected(permisosusuarios.getBoolean("creacion_ordenestrabajo"));
                jCierreOrden.setSelected(permisosusuarios.getBoolean("cierre_ordenestrabajo"));
                jReporteriaAceites.setSelected(permisosusuarios.getBoolean("reporteria_control_aceites"));
                jReporteriaBaterias.setSelected(permisosusuarios.getBoolean("reporteria_control_baterias"));
                jReporteriaLLantas.setSelected(permisosusuarios.getBoolean("reportes_control_llantas"));
                jReporteriaOrden.setSelected(permisosusuarios.getBoolean("reporteria_ordentrabajo"));
                jParametros.setSelected(permisosusuarios.getBoolean("cambios_parametros_empresa"));
                jIngresoDiesel.setSelected(permisosusuarios.getBoolean("ingreso_control_ingresos"));
                jReporteriaDiesel.setSelected(permisosusuarios.getBoolean("reportes_control_ingresos"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Administrador_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    public void getListUsuarios(){
        ResultSet rs=modulo.Listar("Select * from ListadoUsuariosxEmpresa Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'");
        DefaultListModel model=new DefaultListModel();
        if (rs!=null){
            try {
                while (rs.next()){
                    model.addElement(rs.getString("Usuario"));
                    
                }
                jListUsuarios.setModel(model);
                
            } catch (SQLException ex) {
                Logger.getLogger(Administrador_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
