    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jListUsuarios.getSelectedIndex()>-1){
            int i=JOptionPane.showConfirmDialog(jCreacionOrden, "Esta seguro que desea eliminar el usuario "+jListUsuarios.getSelectedValue().toString()+"?", "Sistema de Control de Indicadores", JOptionPane.YES_NO_OPTION);
            if (i==JOptionPane.YES_OPTION){
                if (modulo.Ejecutar("call eliminausuario('"+jListUsuarios.getSelectedValue().toString()+"')").contains("conclu")){
                  getListUsuarios();
                }
                else{
                    JOptionPane.showMessageDialog(jCreacionOrden, "No se pudo eliminar el usuario");
                }
                
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

