public void setListBateriaFromSQL() {
        String comando="select CodBateria from Mantenimiento_IngresoBateriasNuevas WHERE CodBateria LIKE '"+this.id+jTextField1.getText().toUpperCase()+"%'"+" and Estado=2 and IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"' ORDER BY fecha";
        r=m.Listar(comando);      
        model.removeAllElements();
        //final ResultSet r = m.Listar("Select idConcepto, Descripcion from BARR_ConceptoFallo");
        if (r != null) {
            try {
                r.next();
                //r.first();

                model.addElement(r.getString("CodBateria"));
                while (r.next()) {
                    model.addElement(r.getString("CodBateria"));
                }
               
            } catch (SQLException ex) {
                model.add(0, "NO SE ENCONTRARON REGISTROS");
                
                //Logger.getLogger(NuevoRegistro.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        
        jList1.setModel(model);
        jList1.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (jList1.isSelectionEmpty()){}
                else{
                codigobateria = jList1.getSelectedValue().toString();
                
            }}
        });

    }

