    public void getDataFromSQL(){
        new Thread(){
            @Override
            public void run(){
                int i=0;
            int a=dm.getRowCount();
            while(i<a){
                dm.removeRow(0);
                i++;
            }
            //JOptionPane.showMessageDialog(rootPane, funcion);
            jTable1.setModel(dm);
            if (funcion>0&jTextFieldOrdenTrabajo.getText().length()==0)
            {
                getDataFromSQL.getDatafromSQL("Select Id_OrdenTrabajo, Fecha, Id_Bus, Id_Plantel, Tiempo_Total, Hora_Inicio, Hora_Fin, IdEstado from Ordenes_Trabajo Where `IdEstado`='"+funcion+"'  and IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'", 8);
                data=getDataFromSQL.goFirst();
           // JOptionPane.showMessageDialog(rootPane, data[0]);
                dm.addRow(data);
                while(data!=null){
                if (getDataFromSQL.goNext()){
                    data=getDataFromSQL.getDatos();
                    dm.addRow(data);
                }
                else{
                    data=null;
                }
                
                jTable1.setModel(dm);
            }
            }
            else{
                if (funcion<0&jTextFieldOrdenTrabajo.getText().length()==0){
                    getDataFromSQL.getDatafromSQL("Select Id_OrdenTrabajo, Fecha, Id_Bus, Id_Plantel, Tiempo_Total, Hora_Inicio, Hora_Fin, IdEstado from Ordenes_Trabajo Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'", 8);
                }
                else{
                    if (jTextFieldOrdenTrabajo.getText().length()>0){
                        getDataFromSQL.getDatafromSQL("Select Id_OrdenTrabajo, Fecha, Id_Bus, Id_Plantel, Tiempo_Total, Hora_Inicio, Hora_Fin, IdEstado from Ordenes_Trabajo Where `Id_OrdenTrabajo`='"+jTextFieldOrdenTrabajo.getText()+"'  and IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'", 8);
                        //jTextFieldOrdenTrabajo.setText("");
                    }
                    else{
                        ValoresEstaticos.ocultaMensaje();
                     return;
                    }
                }
               data=getDataFromSQL.goFirst();
            // JOptionPane.showMessageDialog(rootPane, data[0]);
                dm.addRow(data); 
               while(data!=null){
                if (getDataFromSQL.goNext()){
                    data=getDataFromSQL.getDatos();
                    dm.addRow(data);
                }
                else{
                    data=null;
                }
                
                jTable1.setModel(dm);
                jTable1.getTableHeader().setBackground(Color.DARK_GRAY);
                jTable1.getTableHeader().setForeground(Color.WHITE);
                ValoresEstaticos.ocultaMensaje();
                } 
            }
            ValoresEstaticos.ocultaMensaje();
            }
        }.start();
        ValoresEstaticos.muestreMensajeEsperar("Recuperando datos solicitados");
    }
    
    
    
    
  
