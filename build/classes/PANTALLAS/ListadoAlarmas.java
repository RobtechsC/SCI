    public void getDataFromSQL(){
        new Thread(){
            @Override
            public void run(){
                try {
            
            SimpleDateFormat df=new SimpleDateFormat("dd-MM-yyyy");
            Date fechai=df.parse(jTextFieldFechaI.getText());
            java.sql.Date fechainicial=new java.sql.Date(fechai.getTime());
            Date fechaf=df.parse(jTextFieldFechaF.getText());
            java.sql.Date fechafinal=new java.sql.Date(fechaf.getTime());
            Object [] columns=new Object[]{"Fecha", "Unidad", "Descripcion"};
            DefaultTableModel dm=new DefaultTableModel(new Object[][]{}, columns);
            if (jTextFieldUnidad.getText().length()>0){
                int unidad=Integer.parseInt(jTextFieldUnidad.getText());
                getDataFromSQL.getDatafromSQL("Select Fecha, Unidad, Descripcion from MonitoreoMovil_Alarmas Where Fecha Between '"+fechainicial+"' and '"+fechafinal+"' Unidad='"+unidad+"'", 3);
            }
            else{
            getDataFromSQL.getDatafromSQL("Select Fecha, Unidad, Descripcion from MonitoreoMovil_Alarmas Where Fecha Between '"+fechainicial+"' and '"+fechafinal+"'", 3);
            }
            
            
            data=getDataFromSQL.goFirst();
            dm.addRow(data);
            while(data!=null){
                if (getDataFromSQL.goNext()){
                    data=getDataFromSQL.getDatos();
                    dm.addRow(data);
                }
                else{
                    data=null;
                }
                
            }
            jTable1.setModel(dm);
            jTable1.getTableHeader().setBackground(Color.DARK_GRAY);
            jTable1.getTableHeader().setForeground(Color.WHITE);
             ValoresEstaticos.ocultaMensaje();
            } catch (ParseException ex) {
            Logger.getLogger(ListadoAlarmas.class.getName()).log(Level.SEVERE, null, ex);
             ValoresEstaticos.ocultaMensaje();
            
        }
            }
        }.start();
        ValoresEstaticos.muestreMensajeEsperar("Recuperando datos solicitados");
        
}
    
    
    
    
  
