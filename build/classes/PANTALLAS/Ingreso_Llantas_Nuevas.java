    private void getLastCodLlanta(){
        ResultSet rs=modulo.Listar("Select * from Mantenimiento_IngresoLlantasNuevas WHERE IdEmpresa="+idEmpresa+" order by Consecutivo DESC Limit 1");
        if (rs!=null){
            try {
            if (rs.first()){
                rs.first();
                String codLlanta=rs.getString("Cod_Llanta");
                int in=codLlanta.indexOf("#")+1;
                char[] dst=new char[codLlanta.length()-in];
                codLlanta.getChars(codLlanta.indexOf("#")+1, codLlanta.length(), dst, 0);
                jLabelCodLLanta.setText(String.valueOf(dst));
                int index=codLlanta.indexOf("-")+1;
                
                char[] d=new char[codLlanta.length()-index];
                
                codLlanta.getChars(index, codLlanta.length(), d, 0);
                
                consecutivo=Integer.parseInt(String.valueOf(d));
                consecutivo++;
                Calendar c=Calendar.getInstance();
                int semana=c.get(Calendar.WEEK_OF_YEAR);
                int año=c.get(Calendar.YEAR);
                String a=String.valueOf(año);
                char[] añ=new char[2];
                a.getChars(2, 4, añ, 0);
                año=Integer.parseInt(String.valueOf(añ));
                jLabelCodigoSugerido.setText(semana+""+año+"-"+consecutivo);
                jTxtCodLlanta.setText(semana+""+año+"-"+consecutivo);
                getListSizes();
            }
            else{
                jLabelCodLLanta.setText("No hay ningun registro");
                Calendar c=Calendar.getInstance();
                int semana=c.get(Calendar.WEEK_OF_YEAR);
                int año=c.get(Calendar.YEAR);
                String a=String.valueOf(año);
                char[] añ=new char[2];
                a.getChars(2, 4, añ, 0);
                año=Integer.parseInt(String.valueOf(añ));
                jLabelCodigoSugerido.setText(semana+""+año+"-"+0001);
                jTxtCodLlanta.setText(semana+""+año+"-"+0001);
                consecutivo=1;
                getListSizes();
            }
                
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
                Logger.getLogger(Ingreso_Llantas_Nuevas.class.getName()).log(Level.SEVERE, null, ex);
            }
            catch (NumberFormatException ex) {
                try {
                    String s=rs.getString("Cod_Llanta");
                    int ind=s.indexOf("#")+1;
                    char [] dt=new char[s.length()-ind];
                    s.getChars(ind, s.length(), dt, 0);
                    jLabelCodLLanta.setText(String.valueOf(dt));
                    Calendar c=Calendar.getInstance();
                    int semana=c.get(Calendar.WEEK_OF_YEAR);
                    int año=c.get(Calendar.YEAR);
                    String a=String.valueOf(año);
                    char[] añ=new char[2];
                    a.getChars(2, 4, añ, 0);
                    año=Integer.parseInt(String.valueOf(añ));
                    jLabelCodigoSugerido.setText(semana+""+año+"-"+0001);
                    jTxtCodLlanta.setText(semana+""+año+"-"+0001);
                    consecutivo=1;
                    Logger.getLogger(Ingreso_Llantas_Nuevas.class.getName()).log(Level.SEVERE, null, ex);
                    getListSizes();
                } catch (SQLException ex1) {
                    JOptionPane.showMessageDialog(rootPane, ex);
                    Logger.getLogger(Ingreso_Llantas_Nuevas.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
        }
        else{
            jLabelCodLLanta.setText("No hay conexion a Internet o no tiene permisos necesarios.");
        }
    }
