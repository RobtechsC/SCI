    private void jButtonReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonReporteActionPerformed
        // TODO add your handling code here:
        Thread t=new Thread(){
            @Override
            public void run(){

                
                try {
                    String archivo="c:/SCI/REPORTES/ReporteSemanalCambLlantasSiguienteSemana.jrxml";
                    SimpleDateFormat df=new SimpleDateFormat("dd-MM-yyyy");
                    Date date1 = null;
                    try {
                        date1 = df.parse(jTxtFecha.getText());
                    } catch (ParseException ex) {
                        Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JasperReport reporte=JasperCompileManager.compileReport(archivo);
                    
                    HashMap map=new HashMap();
                    map.put("FechaInicial", date1);
                    
                    
                    map.put("CodigoEmpresa", ValoresEstaticos.getIdEmpresa());
                    
                    JasperPrint jasperPrint = JasperFillManager.fillReport(reporte, map, new ModuloMySQL().getConexion());
                    JasperViewer jw=new JasperViewer(jasperPrint,false);
                    jw.setTitle("Reporte Cambio de Llantas Siguiente Semana");
                    jw.setExtendedState(Frame.MAXIMIZED_BOTH);
                    jw.setVisible(true);
                   
                    jw.setVisible(true);
                    
                } catch (JRException ex) {
                    JOptionPane.showMessageDialog(null, "Error al generar el reporte");

                    Logger.getLogger(Reporteria.class.getName()).log(Level.SEVERE, null, ex);
                    

                }
            }
        };
        t.start();
    }//GEN-LAST:event_jButtonReporteActionPerformed

