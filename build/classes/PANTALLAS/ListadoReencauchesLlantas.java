    public void getDataFromSQL(){
        
            
            Object [] columns=new Object[]{"Consecutivo","Fecha_Boleta", "Boleta_Proveeddor", "Proveedor","CodLlanta","FechaEntr","Factura","Costo","Estado","User"};
            DefaultTableModel dm=new DefaultTableModel(new Object[][]{}, columns);
            if (jCheckBoxTodas.isSelected()){
                 String s="Select Consecutivo, Fecha_Boleta, Boleta_Proveedor, Proveedor, CodLlanta, Fecha_Entrada, Factura, Costo, Estado, User from Control_ReencauchesLlantas Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"' ORDER BY Fecha_Boleta DESC";
                 getDataFromSQL.getDatafromSQL(s, 10);
                 Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, s);
            }
            else{
                if (jTextFieldFactura.getText().length()>0){
                    String s="Select Consecutivo, Fecha_Boleta, Boleta_Proveedor, Proveedor, CodLlanta, Fecha_Entrada, Factura, Costo, Estado, User from Control_ReencauchesLlantas Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"' and Factura='"+jTextFieldFactura.getText()+"' ORDER BY Fecha_Boleta DESC";
                    getDataFromSQL.getDatafromSQL(s, 10);
                    Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, s);
                }
                else{
                    if (jTextFieldBoleta.getText().length()>0){
                        String s="Select Consecutivo, Fecha_Boleta, Boleta_Proveedor, Proveedor, CodLlanta, Fecha_Entrada, Factura, Costo, Estado, User from Control_ReencauchesLlantas Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"' and Boleta_Tapachula='"+jTextFieldBoleta.getText()+"' ORDER BY Fecha_Boleta DESC";
                        getDataFromSQL.getDatafromSQL(s, 10);
                        Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, s);
                    }
                    else{
                        if (jTxtFecha.getText().length()>0){
                            try {
                                SimpleDateFormat df=new SimpleDateFormat("dd-MM-yyyy");
                                java.sql.Date fechai=new java.sql.Date(df.parse(jTxtFecha.getText()).getTime());
                                String s="Select Consecutivo, Fecha_Boleta, Boleta_Proveedor, Proveedor, CodLlanta, Fecha_Entrada, Factura, Costo, Estado, User from Control_ReencauchesLlantas Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"' and Fecha_Boleta>='"+ fechai+"' ORDER BY Fecha_Boleta DESC";
                                getDataFromSQL.getDatafromSQL(s, 10);
                                Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, s);
                            } catch (ParseException ex) {
                                Logger.getLogger(ListadoReencauchesLlantas.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                        else{
                            JOptionPane.showMessageDialog(rootPane, "No se espefico ningun filtro de datos","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            }
            
            jTable1.removeAll();
            //JOptionPane.showMessageDialog(rootPane, data[0]);
            data=getDataFromSQL.goFirst();
            
           // JOptionPane.showMessageDialog(rootPane, data[0]);
            dm.addRow(data);
            while(getDataFromSQL.goNext()){
                
                    data=getDataFromSQL.getDatos();
                    dm.addRow(data);
                }
                
            
            
            jTable1.setModel(dm);
            jTable1.setBackground(Color.DARK_GRAY);
            jTable1.getTableHeader().setBackground(Color.DARK_GRAY);
            jTable1.getTableHeader().setForeground(Color.white);
    }
           
    
    
    
  
