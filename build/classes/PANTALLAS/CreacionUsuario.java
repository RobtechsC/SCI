    public void getListUsuarios(){
        ResultSet rs=modulo.Listar("Select * from ListadoUsuariosxEmpresa Where IdEmpresa='"+ValoresEstaticos.getIdEmpresa()+"'");
        DefaultListModel model=new DefaultListModel();
        if (rs!=null){
            try {
                while (rs.next()){
                    model.addElement(rs.getString("Usuario"));
                    
                }
                jListUsuarios.setModel(model);
            } catch (SQLException ex) {
                Logger.getLogger(CreacionUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
