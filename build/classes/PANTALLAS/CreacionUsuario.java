    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (testUser()){
            JOptionPane.showMessageDialog(rootPane, "Usuario ya existe para esta u otra empresa por favor escriba otro usuario.","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
            return;
        }
        new Thread(){
            @Override
            public void run(){
                if (Arrays.equals(jPassword.getPassword(), jPasswordconf.getPassword())){
                    
                    if (jPassword.getPassword().length>5)
                    {
                        String s="create USER '"+jUsuario.getText()+"'@'%' IDENTIFIED BY '"+String.valueOf(jPassword.getPassword())+"'";
                        modulo.Ejecutar(s);
                        setGrantsStandardUser();
                        ValoresEstaticos.ocultaMensaje();
                    }
                    else{
                        
                        JOptionPane.showMessageDialog(rootPane, "Password muy corto, debe ser mayor a 5 letras o números","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
                        ValoresEstaticos.ocultaMensaje();
                    }
                }
                else{
                    
                    JOptionPane.showMessageDialog(rootPane, "Password digitado no coincide","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
                    ValoresEstaticos.ocultaMensaje();
                }
            }
        }.start();
        ValoresEstaticos.muestreMensajeEsperar("Creando usuario por favor espere...");
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed
    public void setGrantsStandardUser(){
        String s="CALL setpermisos('"+jUsuario.getText()+"',"+ValoresEstaticos.idempresa+")";
        s=modulo.Ejecutar(s);
        if (s.contains("concluyó")){
            AdministracionUsuarios.setCierreOrden(jCierreOrdenes.isSelected());
            AdministracionUsuarios.setCreacionOrden(jCreacionOrdenes.isSelected());
            AdministracionUsuarios.setIngresoAceites(jIngresoAceites.isSelected());
            AdministracionUsuarios.setIngresoBaterias(jIngresoBaterias.isSelected());
            AdministracionUsuarios.setIngresoDiesel(jIngresoDiesel.isSelected());
            AdministracionUsuarios.setIngresoLlantas(jIngresoLlantas.isSelected());
            AdministracionUsuarios.setParametros(jParametros.isSelected());
            AdministracionUsuarios.setReporteriaAceites(jReporteriaAceites.isSelected());
            AdministracionUsuarios.setReporteriaBaterias(jReporteriaBaterias.isSelected());
            AdministracionUsuarios.setReporteriaDiesel(jReporteriaDiesel.isSelected());
            AdministracionUsuarios.setReporteriaLlantas(jReporteriaLlantas.isSelected());
            AdministracionUsuarios.setReporteriaOrden(jReporteriaOrdenes.isSelected());
            AdministracionUsuarios.setUser(jUsuario.getText());
            if (AdministracionUsuarios.setPermisosUsuario()){
                JOptionPane.showMessageDialog(rootPane, "Usuario registrado correctamente","Sistema de Control de Indicadores",JOptionPane.INFORMATION_MESSAGE);
                getListUsuarios();
                clearData();
            }
             else{
                JOptionPane.showMessageDialog(rootPane, "Error guardando el usuario2","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
            }
            
        }
        else{
            JOptionPane.showMessageDialog(rootPane, s);
            JOptionPane.showMessageDialog(rootPane, "Error guardando el usuario1","Sistema de Control de Indicadores",JOptionPane.ERROR_MESSAGE);
        }
        
    }
